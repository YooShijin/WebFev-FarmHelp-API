def predict_disease(image):
    model = tf.keras.models.load_model("./service/plant_disease_classifier.h5")
    # model = pickle.load(open('service/plant_disease_classifier.pkl', 'rb'))
    test_img = cv2.resize(image, (256, 256))
    test_input = test_img.reshape((1, 256, 256 ,3))
    output = np.argmax(model.predict(test_input))
    percentage = model.predict(test_input).max()
    return {"disease": class_names[output],
            "confidance": percentage
            }


